package advisor;

public class Info {

    String name;
    String categories;
    String link;
    String album;

    public void setName(String name) {
        this.name = name;
    }

    public void setCategories(String categories) {
        this.categories = categories;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public void setAlbum(String album) {
        this.album = album;
    }
}